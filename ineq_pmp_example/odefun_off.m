%-------------------------------------------------------------------%
%--------------------- ODE Functions - Off -------------------------%
%-------------------------------------------------------------------%
function dydt = odefun_off(t, y)
dydt = zeros(4, 1);
u = -y(4)/2;
dydt(1) = y(2);
dydt(2) = -y(1) + y(2) * (1 - y(1)^2) + u;
dydt(3) = -2 * y(1) + y(4) *(1 + 2 * y(1) * y(2));
dydt(4) = -2 * y(2) - y(3) - y(4) * (1 - y(1)^2);
end